package Chess.chessLogic;

/**
 * An object that keeps track of every single move made on the board. This is required in order to implement
 * the Undo feature of the game.
 * Created by aleclueders on 9/23/15.
 */
public class Move {

    public int fromX;
    public int fromY;
    public int toX;
    public int toY;
    public Piece piece;

    public Move(Piece piece, int fromX, int fromY, int toX, int toY) {
        this.piece = piece;
        this.fromX = fromX;
        this.fromY = fromY;
        this.toX = toX;
        this.toY = toY;
    }
}
